class User {

    constructor({id, firstName, lastName, role, city, phone, vk, email, birthDate}) {
        this.id = id
        this.firstName = firstName
        this.lastName = lastName
        this.role = role
        this.city = city
        this.phone = phone
        this.vk = vk
        this.email = email
        this.birthDate = birthDate
    }
}

export default User